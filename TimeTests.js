var timers = {};

// adds time f inti timers[timer]
function timingDecorator(f, timer) {
  return function() {
    var start = new Date();

    var result = f.apply(this, arguments); // (*)

    if (!timers[timer]) timers[timer] = 0;
    timers[timer] += new Date() - start;

    return result;
  }
}

